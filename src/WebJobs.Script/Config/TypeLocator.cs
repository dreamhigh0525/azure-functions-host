using System;
using System.Collections.Generic;

namespace Microsoft.Azure.WebJobs.Script
{
    public class TypeLocator : ITypeLocator
    {
        private Type[] _types;

        public TypeLocator(params Type[] types)
        {
            _types = types;
        }

        public IReadOnlyList<Type> GetTypes()
        {
            return _types;
        }
    }
}
